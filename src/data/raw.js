function isExhibitAnimal(animalName){
    return exhibit_animals.includes(animalName)
}

const Biomes = {
    "Aquatic" : 1,
    "Desert" : 2,
    "Grassland": 3,
    "Taiga": 4,
    "Temperate": 5,
    "Tropical": 6,
    "Tundra": 7,
}

const Regions = {
    "African" : 1,
    "Asian" : 2,
    "European" : 3,
    "North American" : 4,
    "South American" : 5,
    "Central American" : 6,
    "Oceania" : 7,
    "Antarctica" : 8,
}

function getAnimalBiomeIdList(animalName){

    const biomeDictionary = {
        "Aquatic" : aquatic_animals,
        "Desert" : desert_animals,
        "Grassland": grassland_animals,
        "Taiga": taiga_animals,
        "Temperate": temperate_animals,
        "Tropical": tropical_animals,
        "Tundra": tundra_animals,
    }

    const keys = Object.keys(biomeDictionary)
	const biomeNames = keys.filter( (biomeName) => { const biomeList = biomeDictionary[biomeName]; return biomeList.includes(animalName) } )

	if ( biomeNames.length === 0 )
		throw new Error(`Cannot find biome name for ${animalName}`)

	return biomeNames.map( (name) => Biomes[name] )
}

function getAnimalRegionIdList(animalName){

    const regionDictionary = {
        "African" : african_animals,
        "Asian" : asian_animals,
        "European" : european_animals,
        "North American" : na_animals,
        "South American" : sa_animals,
        "Central American" : ca_animals,
        "Oceania" : oceania_animals,
        "Antarctica" : antarctica_animals,
    }

    const keys = Object.keys(regionDictionary)
	const regionNames = keys.filter( (regionName) => { const regionList = regionDictionary[regionName]; return regionList.includes(animalName) } )

    if (regionNames.length === 0)
      throw new Error(`Cannot find region name for ${animalName}`)

    return regionNames.map( (name) => Regions[name])
}

const n_all_animals = all_animals.map( (animalName, index) => {

    const is_ExhibitAnimal = isExhibitAnimal(animalName)

    const item = {
		id: index + 1,
		name: animalName,
        is_exhibit_animal: is_ExhibitAnimal,
        biomes: is_ExhibitAnimal ? "" : getAnimalBiomeIdList(animalName),
        regions: is_ExhibitAnimal ? "" : getAnimalRegionIdList(animalName),
    }

    return item
})

